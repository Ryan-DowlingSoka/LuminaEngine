    void FCodeGenerator::WriteHeaderContent(FCodeWriter& Writer, FReflectedHeader* Header)
    {
        const auto& Types = ReflectionDatabase->ReflectedTypes.at(Header);
        const eastl::string FileID = Names::MakeFileIDForHeaderPath(Header->HeaderPath);

        Writer.Line("#pragma once");
        Writer.Line();
        EmitFileBanner(Writer);

        Writer.Line("#include \"Core/Reflection/ReflectedTypeAccessors.h\"");
        Writer.BlankLines(2);

        // Guard against double-inclusion without a #pragma once in the upstream header.
        Writer.Linef("#ifdef %s_generated_h", FileID.c_str());
        Writer.Line("#error Already included, missing #pragma once");
        Writer.Line("#endif");
        Writer.Linef("#define %s_generated_h", FileID.c_str());
        Writer.BlankLines(3);

        // Forward declarations + Construct_* decls + DECLARE_CLASS macros.
        for (const auto& Type : Types)
        {
            Type->DefineInitialHeader(Writer, FileID);
        }

        Writer.BlankLines(3);
        Writer.Line("//--------------------------------------------------------------------------------------");
        Writer.BlankLines(3);

        // GENERATED_BODY expansion macros.
        for (const auto& Type : Types)
        {
            Type->DefineSecondaryHeader(Writer, FileID);
        }

        Writer.BlankLines(3);
        Writer.Line("#undef CURRENT_FILE_ID ");
        Writer.Linef("\t #define CURRENT_FILE_ID %s", FileID.c_str());
    }

