    FCodeGenerator::FCodeGenerator(FReflectedWorkspace* InWorkspace, const FReflectionDatabase& Database)
        : Workspace(InWorkspace)
        , ReflectionDatabase(&Database)
    {
    }

    void FCodeGenerator::GenerateCode()
    {
        // Per-project accumulators:
        //   - Unity cpp pools every dirty header's .generated.cpp include.
        //   - Lua API string is the concatenated Luau type definitions.
        eastl::hash_map<FReflectedProject*, eastl::string> UnityPerProject;
        eastl::hash_set<FReflectedProject*>                DirtyProjects;
        eastl::string                                      LuaApiString;

        {
            FCodeWriter LuaWriter;

            for (const auto& [Header, _] : ReflectionDatabase->ReflectedTypes)
            {
                eastl::string& Unity = UnityPerProject[Header->Project];
                if (Unity.empty())
                {
                    Unity += "#include \"pch.h\"\n";
                }
                Unity += "#include \"" + Header->FileName + ".generated.cpp\"\n";

                WriteLuaApiContent(LuaWriter, Header);

                if (Header->bDirty)
                {
                    DirtyProjects.insert(Header->Project);
                    GenerateHeaderFile(Header);
                    GenerateSourceFile(Header);
                }
            }

            LuaApiString = LuaWriter.Release();
        }

        // Write per-project Lua definition files. All projects share a single
        // Lua API string today; that matches the previous behaviour.
        for (auto& Project : Workspace->ReflectedProjects)
        {
            WriteLuaDefinitionsFile(Project.get(), LuaApiString);
        }

        for (auto* DirtyProject : DirtyProjects)
        {
            WriteUnityBuildFile(DirtyProject, UnityPerProject[DirtyProject]);
        }

        // Stub guard: every reflection-enabled project lists ReflectionUnity.gen.cpp
        // in its vcxproj sources, so the file MUST exist on disk even if the
        // project has zero reflected types (e.g. a freshly-templated game project).
        // We only write the stub if no file exists yet — this keeps incremental
        // builds fast (no mtime touch) and lets the dirty path above overwrite
        // it the moment the project actually gets reflected types.
        //
        // The __has_include guard makes the same stub work whether the host
        // project has a PCH (Runtime/Editor/Lumina include "pch.h") or not
        // (game-template projects don't set one up).
        for (auto& Project : Workspace->ReflectedProjects)
        {
            const eastl::string Path = MakeUnityPath(Workspace->GetPath(), *Project);
            if (std::filesystem::exists(std::filesystem::path(Path.c_str())))
            {
                continue;
            }

            WriteTextFile(Path,
                "// Reflection unity stub.\n"
                "// This project has no reflected types yet; this file exists\n"
                "// only so the vcxproj's source list resolves. The Reflector\n"
                "// will overwrite it the moment a reflected type appears.\n"
                "#if __has_include(\"pch.h\")\n"
                "    #include \"pch.h\"\n"
                "#endif\n");
        }
    }

